const promptLookup = new Map();

function addPromptPair(pair) {
  promptLookup.set(pair.hi, pair);
  promptLookup.set(pair.en, pair);
}

Object.values(truthPromptCatalog).forEach((prompts) => prompts.forEach(addPromptPair));
Object.values(darePromptCatalog).forEach((prompts) => prompts.forEach(addPromptPair));

export function localizeTruthOrDarePrompt(prompt, language = 'en') {
  const promptPair = promptLookup.get(prompt);

  if (!promptPair) {
    return prompt;
  }

  return promptPair[language] || promptPair.en;
}
